/*
STL - Standard Template Library
Three basic components:
1. Containers   :   These type of classes store objects    
2. Algorithms   :   These act on the containers
3. Iterators    :   These act like pointer to elements of the container and can be used to access the elements
                    They are like abstracton/generalization of actual pointers
                    In these unlike pointers we need not to define type, therefor it is considered as generalized
                    Here, '*'(dereference) operator is overloaded

CONTAINER:
    Sequence Containers     - vector, stack, dequeue
    Associative container   - map, set

ITERATOR:
    Random Access Iterator
    Bidirectional Iterator
    Forward Iterator
    Input Iterator
    Output Iterator
*/